<h2 id="Simple">Simple</h2>
<p>A new kernel for your EZ-Flash Omega</p>
<p>
Simple comes with loads of new options and plugins from the era of PogoShell (remember when that was
popular? <img src="/assets/img/smileys/wink.png">).<br />
It also comes with a new theme that in my opinion looks better than the stock firmware.<br />
Using this kernel you can view images, listen to music, read text documents, and play many more
systems such as the Sega Master System or Sinclair ZX Spectrum!<br />
There is also a variant available for the EZ-Flash Omega: Definitive Edition!<br />
</p>

